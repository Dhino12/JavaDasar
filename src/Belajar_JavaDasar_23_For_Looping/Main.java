package Belajar_JavaDasar_23_For_Looping;

public class Main {
    public static void main(String[] args) {

        System.out.println("\nLooping Pertama\n");
        for (int a = 1; a <= 10 ; a++){
            System.out.println("Masuk For Looping: " + a);
        }
        System.out.println("\nLooping Kedua\n");
        for (int b = 10; b >= 5; b--){
            System.out.println("Masuk For Looping: " + b);
        }
    }
}
