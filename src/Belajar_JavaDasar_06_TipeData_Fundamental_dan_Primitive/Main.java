package Belajar_JavaDasar_06_TipeData_Fundamental_dan_Primitive;

public class Main {
    public static void main(String[] args) {
        //Tipe Data Primitive
            // int | Double | Float | Char | Byte | Boolean | Short
        // Tipe Data Fundamental
            //String

        //Integer(Bilangan Bulat) ==========
        int i = 1;
        System.out.println("============= Integer ============");
        System.out.println("Nilai Integer i: " + i);
        System.out.println("Nilai Integer MAX: " + Integer.MAX_VALUE);
        System.out.println("Nilai Integer MIN: " + Integer.MIN_VALUE);
        System.out.println("Nilai Integer bytes: " + Integer.BYTES + " bytes");
        System.out.println("Nilai Integer bit: " + Integer.SIZE + " bit");
        System.out.println("i = " + i);

        //Byte(Bilangan Bulat) ==========
        byte a = 1;
        System.out.println("============= Byte ============");
        System.out.println("Nilai Byte a: " + a);
        System.out.println("Nilai Byte MAX: " + Byte.MAX_VALUE);
        System.out.println("Nilai Byte MIN: " + Byte.MIN_VALUE);
        System.out.println("Nilai Byte bytes: " + Byte.BYTES + " bytes");
        System.out.println("Nilai Byte bit: " + Byte.SIZE + " bit");

        //Short(Bilangan Bulat) ==========
        short b = 1;
        System.out.println("============= Short ============");
        System.out.println("Nilai Short b: " + b);
        System.out.println("Nilai Short MAX: " + Short.MAX_VALUE);
        System.out.println("Nilai Short MIN: " + Short.MIN_VALUE);
        System.out.println("Nilai Short bytes: " + Short.BYTES + " bytes");
        System.out.println("Nilai Short bit: " + Short.SIZE + " bit");

        //Long(Bilangan Bulat) ==========
        long c = 1;
        System.out.println("============= Long ============");
        System.out.println("Nilai Long c: " + c);
        System.out.println("Nilai Long MAX: " + Long.MAX_VALUE);
        System.out.println("Nilai Long MIN: " + Long.MIN_VALUE);
        System.out.println("Nilai Long bytes: " + Long.BYTES + " bytes");
        System.out.println("Nilai Long bit: " + Long.SIZE + " bit");

        //Double(Bilangan Bulat) ==========
        double d = 1.1d;
        System.out.println("============= Double ============");
        System.out.println("Nilai Double d: " + d);
        System.out.println("Nilai Double MAX: " + Double.MAX_VALUE);
        System.out.println("Nilai Double MIN: " + Double.MIN_VALUE);
        System.out.println("Nilai Double bytes: " + Double.BYTES + " bytes");
        System.out.println("Nilai Double bit: " + Double.SIZE + " bit");

        //Float(Bilangan Bulat) ==========
        float e = 1.3F;
        System.out.println("============= Float ============");
        System.out.println("Nilai Float e: " + e);
        System.out.println("Nilai Float MAX: " + Float.MAX_VALUE);
        System.out.println("Nilai Float MIN: " + Float.MIN_VALUE);
        System.out.println("Nilai Float bytes: " + Float.BYTES + " bytes");
        System.out.println("Nilai Float bit: " + Float.SIZE + " bit");

        //Char(Bilangan Bulat) ==========
        char f = 'c';
        System.out.println("============= Char ============");
        System.out.println("Nilai Char f: " + f);
        System.out.println("Nilai Char MAX: " + Character.MAX_VALUE);
        System.out.println("Nilai Char MIN: " + Character.MIN_VALUE);
        System.out.println("Nilai Char bytes: " + Character.BYTES + " bytes");
        System.out.println("Nilai Char bit: " + Character.SIZE + " bit");

        //Boolean(Bilangan Bulat) ==========
        boolean g = true;
        System.out.println("============= Boolean ============");
        System.out.println("Nilai Boolean g: " + g);
        System.out.println("Nilai Boolean MAX: " + Boolean.TRUE);
        System.out.println("Nilai Boolean MIN: " + Boolean.FALSE);
        System.out.println("Nilai Boolean bytes: " + 1 + " bytes");
        System.out.println("Nilai Boolean bit: " + 1 + " bit");
    }
}
