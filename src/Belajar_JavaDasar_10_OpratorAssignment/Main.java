package Belajar_JavaDasar_10_OpratorAssignment;

public class Main {
    public static void main(String[] args) {
        int a = 10 ; // Operator Assignment
        System.out.println("Nilai A = " + a);
        a += 10;
        System.out.println("Nilai A = " + a);
        a -= 10;
        System.out.println("Nilai A = " + a);
        a /= 10;
        System.out.println("Nilai A = " + a);
        a *= 10;
        System.out.println("Nilai A = " + a);
        a %= 10;
        System.out.println("Nilai A = " + a);
    }
}
