package Belajar_JavaDasar_05_Variable_Assigment_Declaration;

public class Main {
    public static void main(String[] args) {
        //Tipe Data
            // int | Double | Float | String | Char | Byte |
        int x = 10; // Assigment = menaruh data ke variable
        System.out.println("Umur latina = " + x);
        System.out.println("selisih umur latina dan hatena = " + (x % 3) + " Tahun");

        //Declaration
        float a = 10.5F;
        System.out.println("a * x = " + (a * x));
    }
}
